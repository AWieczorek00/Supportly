CREATE TABLE ADDRESS
(
    ID            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CITY          VARCHAR2(255) NOT NULL,
    STREET        VARCHAR2(255) NOT NULL,
    ZIP_CODE      VARCHAR2(10)  NOT NULL,
    STREET_NUMBER VARCHAR2(255) NOT NULL -- Changed to VARCHAR2 for consistency and flexibility
);

-- Tworzenie tabeli Company
CREATE TABLE COMPANY
(
    ID           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME         VARCHAR2(255)       NOT NULL,
    NIP          VARCHAR2(13) UNIQUE,
    ADDRESS_ID   NUMBER,
    PHONE_NUMBER VARCHAR2(9)         NOT NULL,
    EMAIL        VARCHAR2(255)       NOT NULL,
    REGON        VARCHAR2(14) UNIQUE NOT NULL,
    CONSTRAINT FK_COMPANY_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES ADDRESS (ID) ON DELETE CASCADE
);

-- Tworzenie tabeli Agreement
CREATE TABLE AGREEMENT
(
    ID               NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    COMPANY_ID       NUMBER,
    AGREEMENT_NUMBER VARCHAR2(50)  NOT NULL,
    DATE_FROM        DATE          NOT NULL,
    DATE_TO          DATE          NOT NULL,
    PERIOD           VARCHAR2(255) NOT NULL,
    CONSTRAINT FK_AGREEMENT_COMPANY FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY (ID) ON DELETE CASCADE
);