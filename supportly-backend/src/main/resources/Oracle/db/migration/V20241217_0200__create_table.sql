CREATE TABLE EMPLOYEE
(
    ID               NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    FIRST_NAME       VARCHAR2(255) NOT NULL,
    SECOND_NAME      VARCHAR2(255),
    LAST_NAME        VARCHAR2(255) NOT NULL,
    EMAIL            VARCHAR2(255),
    PHONE_NUMBER     NUMBER       NOT NULL, -- Oracle NUMBER can handle large integers
    DATA_OF_CREATION DATE         NOT NULL
);

CREATE TABLE CLIENT
(
    ID               NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME             VARCHAR2(255) NOT NULL,
    NIP              VARCHAR2(13),
    ADDRESS          VARCHAR2(255) NOT NULL,
    CITY             VARCHAR2(255) NOT NULL,
    ZIP_CODE         VARCHAR2(255) NOT NULL,
    STREET_NUMBER    VARCHAR2(255) NOT NULL,
    APARTMENT_NUMBER VARCHAR2(255),
    PHONE_NUMBER     VARCHAR2(9)   NOT NULL,
    EMAIL            VARCHAR2(255) NOT NULL,
    TYPE             VARCHAR2(255) NOT NULL
);

CREATE TABLE ACTIVITIES
(
    ID        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME      VARCHAR2(255) NOT NULL,
    ATTENTION VARCHAR2(255),
    DONE      NUMBER(1)     NOT NULL -- Use NUMBER(1) for boolean-like values (0 or 1)
);

CREATE TABLE PARTS_TEMPLATE
(
    ID    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME  VARCHAR2(255) NOT NULL,
    PRICE NUMBER        NOT NULL, -- Use NUMBER for float/decimal values
    TAX   NUMBER
);

CREATE TABLE ACTIVITIES_TEMPLATE
(
    ID   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(255) NOT NULL
);

CREATE TABLE ROLE
(
    ID          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME        VARCHAR2(255) UNIQUE            NOT NULL,
    DESCRIPTION VARCHAR2(255)                   NOT NULL,
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL, -- Use TIMESTAMP for CURRENT_TIMESTAMP
    UPDATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE TASK
(
    ID            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME          VARCHAR2(255) NOT NULL,
    EXECUTION_TIME DATE,
    DONE          NUMBER(1), -- Use NUMBER(1) for boolean-like values (0 or 1)
    EMPLOYEE_ID   NUMBER,
    CONSTRAINT FK_TASK_EMPLOYEE FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (ID)
);

CREATE TABLE ADM_USER
(
    ID        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    FULL_NAME VARCHAR2(255)                   NOT NULL,
    EMAIL     VARCHAR2(100) UNIQUE            NOT NULL,
    PASSWORD  VARCHAR2(255)                   NOT NULL,
    CREATE_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL, -- Use TIMESTAMP for CURRENT_TIMESTAMP
    UPDATE_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ROLE_ID   NUMBER                            NOT NULL,
    CONSTRAINT FK_USER_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLE (ID)
);

CREATE TABLE PART
(
    ID     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME   VARCHAR2(255),
    PRICE  NUMBER,
    TAX    NUMBER,
    AMOUNT NUMBER -- NUMBER is a good general-purpose type for integers in Oracle
);

-- "order" is a reserved keyword in Oracle, so it needs to be enclosed in double quotes.
CREATE TABLE "ORDER"
(
    ID                NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CLIENT_ID         NUMBER,
    DATE_OF_ADMISSION DATE,
    DATE_OF_EXECUTION DATE,
    MAN_HOUR          NUMBER,
    DISTANCE          NUMBER,
    PRIORITY          VARCHAR2(255),
    STATUS            VARCHAR2(255),
    PERIOD            VARCHAR2(255),
    NOTE              VARCHAR2(255),
    CONSTRAINT FK_ORDER_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT (ID)
);
